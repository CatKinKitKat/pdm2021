package com.ipbeja.easymed.Objects;

/**
 * The type Image slider.
 */
public class ImageSlider {

    /**
     * The Image.
     */
    int Image;

    /**
     * Instantiates a new Image slider.
     */
    public ImageSlider() {
    }

    /**
     * Instantiates a new Image slider.
     *
     * @param image the image
     */
    public ImageSlider(int image) {
        Image = image;
    }

    /**
     * Gets image.
     *
     * @return the image
     */
    public int getImage() {
        return Image;
    }

    /**
     * Sets image.
     *
     * @param image the image
     */
    public void setImage(int image) {
        Image = image;
    }
}
